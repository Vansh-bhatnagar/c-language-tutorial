// using bitwise operators
#include <stdio.h>
void main()	
{
	int a;
	char b;
	char c[10];
	printf("\n   %d ",sizeof(a));
	printf("\n   %d ",sizeof(int));
	printf("\n   %d ",sizeof(b));
	printf("\n   %d ",sizeof(char));
	printf("\n   %d ",sizeof(c));
	printf("\n   %d ",sizeof(char[10]));
	printf("\n   %d ",sizeof(int[10]));
	
}
