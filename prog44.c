// for loop example
#include <stdio.h>
void main()
{
	int c;
	for(c=1;c<=5;c++)
	{
		printf("\n %d---> ***Hello welcome***",c);
	}
	printf("\n**************************************************");
	for(c=5;c>=1;c--)
	{
		printf("\n %d---> ***Hello welcome***",c);
	}
	printf("\n END");
}
